// Usamos o comando = parseFloat ('Numero aqui'); para definir que queremos numero depois da vírgula.EX.:
// Usamos o comando = parseInt ('Numero aqui'); para definir que vamos usar somente o número inteiro.
// Caso ache melhor, podemos usar somente = Number('Numero aqui'); que o próprio aplicativo vai entender o que é o número. 


const num1 = parseFloat('5.50');
console.log(num1);
const num2 = parseInt('5.50');
console.log(num2);
const num3 = Number('5.50');
console.log(num3);


